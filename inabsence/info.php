            <p>
            inabsence is a durational text based performance 
            located in between spaces as a proximate non-site, 
            relegated to Internet domain. There is no beginning 
            but rather multiple points of entry. The history of 
            the text is ongoing; each entry marks a new point in 
            temporality within an ever-expanding loop or network. 
            Time is non linear, time is marked by the transmitter 
            as instances that mark intervals. inabsence is an 
            experiment in automatic site writing within time and 
            space, dislocated from context. An extensive inventory, 
            a logging of time, labour and futile action. A constant 
            feeding of data to the machine. A premonition of the 
            future, or a recollection of the past decentralised 
            from point of origin, emphasising the present upon the 
            moment of access. The text stands in for an absent body, 
            a trace of action, of presence swiftly relegated to the 
            past as it is received or experienced. 
            </p>

            <p>
            The online exhibition is accompanied by an invitation 
            to share a moment of collective presence in a series 
            of afternoons dedicated to silent writing, reading and 
            reflection in a physical site, a sharing of space and 
            time together in an embodied nonline space.</p>


