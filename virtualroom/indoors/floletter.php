        <h4>Floriane's letter to Lola</h4>
        <p>Dear Lola,</p>

        <p>I have to confess that I don't remember 
            very well what you look like. The last 
            time I saw you, you still had hair, but 
            it seems that you went bald at some point
             in between our last encounter and the 
             start of the confinement. At least,
              that's how you are now on your profile 
              picture on Facebook. I do have a vague 
              memory of having Skype video call with 
              you, as we were working on the Indoors 
              exhibition, but lately your voice either 
              personifies my phone, or your whole being 
              is reduced to the forever grey received 
              message bubbles on Messenger. I increasingly
               sent messages to the wrong recipient the 
               last weeks. I pointed out it was because 
               all my friends looked the same in this black 
               cubicle, that my brain cannot notice the
                tiny clues assigning identity to the 
                various voices that today inhabit my 
                smartphone. I have to confide however 
                that I have never talked to so many 
                people at the same time on social media.<br>
The forced disconnection that happened all of a 
sudden created that craving for more connections 
in the virtual space. As if the virtual world 
could be a substitute for the world we are deprived 
of at the moment. As if the virtual world could 
help us be less lonely. If it was the case, then 
I wouldn't have felt lonely the couple of months 
before the confinement. But what we should remember  
of what is happening is not the power of technology 
to connect us, but our ability to feel and react as 
a social group. By (re-)activating the fundamental 
social capability of a group, we never have been 
alone facing the fear of the virus. We form a tight 
organism, acting all as one in a cohesive manner. 
The current situation shows the need for cohesiveness 
in the group to sustain the health and the wealth of 
each of its individuals; and that individualism is a 
dangerous behaviour that can have deadly repercussions 
on others. I do not mean, of course, that individuality 
should not be cultivated as it is those individualities 
that make a collective intelligence possible. As a 
librarygathers a diversity of knowledge and skills.</p>

<p>Speaking of libraries, public ones are indeed one 
    of the most space missing today in our lives. 
    Libraries allow another way of consuming information 
    than on the web, implying the slow pace of reading long 
    length books. Of course, on the web, one can find 
    books, to read or to listen to, but there is a global 
    tendency to fast consume knowledge through short 
    articles, Quora questions, Wikipedia or even Facebook 
    posts. It is a habit we took to answer questions we 
    have with fast fact checking. Questions that sometimes
     we have to ask again and again without managing to
      remember the answer. Libraries can't offer that access 
      to fast knowledge but on the other hand, they can 
      spark curiosity and interests, as each book has an 
      equal presence and accessibility on the shelves.<br>
Another essential part of the public library role is its
 position as a hub for mind meetings: in most cases, 
 libraries are spaces used to connect people through 
 activities such as lectures or courses. The  
 accessibility to everyone and of a wide variety of 
 subjects are strengths that allow improbable encounters 
 that can sometimes shake our vision of the world up. 
 Unfortunately, our world views are rarely challenged 
 on the web: we tend to be clustered in a specific 
 community and algorithms keep showing us over and over 
 again the same content, close to our opinions and 
 beliefs. The repetition of a same perspective on 
 reality does not help us to have an overview of 
 what the world and the people around us truly are. 
 Reality becomes an illusion that correspond to what 
 we would like it to be.</p>

<p>I choose this photograph of a corridor because I 
    found the concept of that type of space a 
    fascinating one and I believe that you might 
    like it as well, Lola. Corridors are a recurrent 
    type of space that has for only purpose to 
    connect multifunctional spaces. They are public 
    spaces, non-spaces, of chance meeting, latency, 
    waiting, passing by, impersonal, offering many 
    options and none at the same time. They are 
    defined by a temporality: either appointed by a 
    rendezvous, or by the speed, as the distance 
    divided by the time, going from point A to point 
    B. Moreover, a movement characterizes them, 
    forward or still, but both in the expectancy 
    of what will happen next. We rarely focus on the 
    present moment of being in the corridor, as the 
    street can be for example places of wanders or 
    inhabitancy. The corridor is nowadays calculated 
    in the most efficient way: it is most often a bare 
    white cubicle excavation in which an X number of 
    bodies can pass at the same time depending on the 
    demands of the type of building it is part of. 
    Almost as a pipe, the width of the gap defines 
    the pressure of whatever passes through. <br>
Its temporality is an in-between time, which in 
capitalist societies is calculated to be reduced 
to its minimum in order to assign more time to 
production and consumption. No time is then left 
to imagine what would or could be another society, 
to imagine ourselves differently.<br>
Have you ever dreamed of corridors leading to 
nowhere, and extracting themselves from the basic 
laws of our occidental society? Because I have, 
incessantly.</p>

<p>I hope you are doing well and that whatever
     will come after this time will help us imagine 
     and create another world, still with a lot of 
     libraries and corridors, but fewer inequalities.</p>

<p>Your dear friend, <br>
Flo</p>

