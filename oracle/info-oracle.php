            Departing from the idea of chance and mysticism conveyed in the Bibliomancy
            practice, from which I take the rules and aleatory elements to develop my work.
            The Bibliomancy technique appears in my work in different ways: f.ex., I made a
            8 meters long text printed with a labeller machine using the books in the shelf
            dedicated to printmaking, fiction, and photography in the old KHiB library, using
            sentences taken at random from the books. Each book was answering the question
            ‘How to Write What is Already Written?’, and the length of the answer printed in
            the label, determined which book was the next one to answer the question. Also, I
            performed a Bibliomancy Oracle in the KMD Library (UiB) where people would ask
            a question to a book that we would take together from the shelf, write the answer
            in a stripe of paper and place it underneath the book in the shelf.
            </p>
